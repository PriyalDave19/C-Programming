#include<stdio.h>
void main(){
	
	int a =10;
	printf("Actual Value of a = %d \n",a);
	
	a+=5;
	printf("Addition of a = %d \n",a);
	
	a-=5;
	printf("Substraction of a = %d \n",a);
	
	a*=5;
	printf("Mulplication of a = %d \n",a);
	
	a/=5;
	printf("Division of a = %d \n",a);
	
	a%=5;
	printf("Modulus of a = %d \n",a);
	
 
}
